package com.zpy.pojo;

public class User {
    public String client_name, client_code, client_id, client_address, client_telephone, client_spouse, spouse_id, spouse_telephone;

    public String getClient_name() {
        return client_name;
    }

    public void setClient_name(String client_name) {
        this.client_name = client_name;
    }

    public String getClient_code() {
        return client_code;
    }

    public void setClient_code(String client_code) {
        this.client_code = client_code;
    }

    public String getClient_id() {
        return client_id;
    }

    public void setClient_id(String client_id) {
        this.client_id = client_id;
    }

    public String getClient_address() {
        return client_address;
    }

    public void setClient_address(String client_address) {
        this.client_address = client_address;
    }

    public String getClient_telephone() {
        return client_telephone;
    }

    public void setClient_telephone(String client_telephone) {
        this.client_telephone = client_telephone;
    }

    public String getClient_spouse() {
        return client_spouse;
    }

    public void setClient_spouse(String client_spouse) {
        this.client_spouse = client_spouse;
    }

    public String getSpouse_id() {
        return spouse_id;
    }

    public void setSpouse_id(String spouse_id) {
        this.spouse_id = spouse_id;
    }

    public String getSpouse_telephone() {
        return spouse_telephone;
    }

    public void setSpouse_telephone(String spouse_telephone) {
        this.spouse_telephone = spouse_telephone;
    }
}
